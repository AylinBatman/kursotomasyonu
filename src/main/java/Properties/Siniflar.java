package Properties;

public class Siniflar {
	 private String sinifId;
	 private String sinifAdi;
	 private String ogrenciSayisi;
	 private String sinifSeviye;
	 private String sinifSira;
	public String getSinifSira() {
		return sinifSira;
	}
	public void setSinifSira(String sinifSira) {
		this.sinifSira = sinifSira;
	}
	public String getSinifId() {
		return sinifId;
	}
	public void setSinifId(String sinifId) {
		this.sinifId = sinifId;
	}
	public String getSinifAdi() {
		return sinifAdi;
	}
	public void setSinifAdi(String sinifAdi) {
		this.sinifAdi = sinifAdi;
	}
	public String getOgrenciSayisi() {
		return ogrenciSayisi;
	}
	public void setOgrenciSayisi(String ogrenciSayisi) {
		this.ogrenciSayisi = ogrenciSayisi;
	}
	public String getSinifSeviye() {
		return sinifSeviye;
	}
	public void setSinifSeviye(String sinifSeviye) {
		this.sinifSeviye = sinifSeviye;
	}
	
}